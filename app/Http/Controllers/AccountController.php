    public function store(Request $request)
    {
        $this->validate($request,[
            'name'=>'required',
            'password'=>'required',
            'email'=>'email|required|unique:users',
            'type'=>'required'
        ]);
        User::create(['name'=>$request->name, 'password'=>Hash::make($request->password), 'email'=>$request->email, 'type'=>$request->type]);
        return back();
    }

